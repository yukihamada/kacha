def generate_kagi_band(outdir: str):
    """Generate KAGI Band 24×12mm 2-layer Gerbers."""
    import copy

    BW, BH = 24.0, 12.0
    os.makedirs(outdir, exist_ok=True)

    # Save originals and swap in band data
    orig_cpl = CPL_DATA.copy()
    orig_fp  = COMPONENT_FOOTPRINTS.copy()
    orig_bw  = BOARD_W
    orig_bh  = BOARD_H

    # Monkeypatch globals for band dimensions
    import gen_gerbers as self_mod
    self_mod.CPL_DATA = BAND_CPL
    self_mod.COMPONENT_FOOTPRINTS = BAND_FOOTPRINTS
    self_mod.BOARD_W = BW
    self_mod.BOARD_H = BH

    files = {
        "kagi-band-F_Cu.gbr":      gen_copper_layer("F.Cu", True, True),
        "kagi-band-B_Cu.gbr":      gen_copper_layer("B.Cu", False, False),
        "kagi-band-F_Mask.gbr":    gen_mask_layer(True),
        "kagi-band-B_Mask.gbr":    gen_mask_layer(False),
        "kagi-band-F_SilkS.gbr":   gen_silkscreen(True),
        "kagi-band-B_SilkS.gbr":   gen_silkscreen(False),
        "kagi-band-Edge_Cuts.gbr":  gen_edge_cuts(),
        "kagi-band.drl":            gen_drill_file(),
    }

    # Restore
    self_mod.CPL_DATA = orig_cpl
    self_mod.COMPONENT_FOOTPRINTS = orig_fp
    self_mod.BOARD_W = orig_bw
    self_mod.BOARD_H = orig_bh

    print(f"\nGenerating KAGI Band Gerbers → {outdir}/")
    for fname, content in files.items():
        fpath = os.path.join(outdir, fname)
        with open(fpath, "w") as f:
            f.write(content)
        print(f"  ✓ {fname}  ({len(content):,} bytes)")

    zippath = outdir + ".zip"
    with zipfile.ZipFile(zippath, "w", zipfile.ZIP_DEFLATED) as zf:
        for fname in files:
            zf.write(os.path.join(outdir, fname), fname)
    print(f"\n  📦 {zippath}")
    return zippath


