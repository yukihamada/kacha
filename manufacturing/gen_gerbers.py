def rotate(dx: float, dy: float, angle_deg: float) -> tuple[float, float]:
    """Rotate offset by angle (degrees CW in PCB convention)."""
    a = math.radians(angle_deg)
    rx =  dx * math.cos(a) + dy * math.sin(a)
    ry = -dx * math.sin(a) + dy * math.cos(a)
    return rx, ry


# ── Gerber generators ─────────────────────────────────────────────────────────

def gen_edge_cuts() -> str:
    """Generate Edge.Cuts layer: 55×55mm board with 1.5mm corner radius."""
    lib = ApertureLib()
    a_line = lib.define_circle(0.1)  # thin outline line

    lines = [gerber_header("Profile,NP")]
    lines.append(lib.definitions())
    lines.append(f"D{a_line}*")
    lines.append("G01*")  # linear interpolation

    # Board outline as 4 lines (simplified, without arcs for compatibility)
    x0, y0 = 0.0, 0.0
    x1, y1 = BOARD_W, BOARD_H
    corners = [
        (x0, y0), (x1, y0), (x1, y1), (x0, y1), (x0, y0)
    ]
    lines.append(f"{gxy(corners[0][0], corners[0][1])}D02*")
    for cx, cy in corners[1:]:
        lines.append(f"{gxy(cx, cy)}D01*")

    lines.append(GERBER_FOOTER)
    return "\n".join(lines)


def gen_copper_layer(layer_name: str, include_pads: bool = True,
                     is_top: bool = True) -> str:
    """Generate copper layer with component pads."""
    lib = ApertureLib()

    pad_ops = []  # list of (aperture_key, x, y, operation) built after lib populated

    if include_pads:
        for ref, (cx, cy, rot) in CPL_DATA.items():
            fp_func = COMPONENT_FOOTPRINTS.get(ref)
            if not fp_func:
                continue
            for (dx, dy, pw, ph, shape) in fp_func():
                rdx, rdy = rotate(dx, dy, rot)
                px = cx + rdx
                py = cy + rdy
                if shape == "rect":
                    aid = lib.define_rect(pw, ph)
                elif shape == "circle":
                    aid = lib.define_circle(pw)
                elif shape == "obround":
                    aid = lib.define_obround(pw, ph)
                else:
                    aid = lib.define_rect(pw, ph)
                pad_ops.append((aid, px, py))

    func = f"Copper,L1,Top" if is_top else f"Copper,L4,Bot"
    lines = [gerber_header(func)]
    lines.append(lib.definitions())
    lines.append("G01*")

    current_apt = -1
    for (aid, px, py) in pad_ops:
        if aid != current_apt:
            lines.append(f"D{aid}*")
            current_apt = aid
        lines.append(f"{gxy(px, py)}D03*")  # D03 = flash

    lines.append(GERBER_FOOTER)
    return "\n".join(lines)


def gen_copper_inner(layer: int) -> str:
    """Generate empty inner copper layers (GND/PWR planes — unrouted)."""
    lib = ApertureLib()
    a_fill = lib.define_rect(BOARD_W - 1.0, BOARD_H - 1.0)
    func = f"Copper,L{layer},Inr"
    lines = [gerber_header(func)]
    lines.append(lib.definitions())
    lines.append("G01*")
    # Solid copper pour covering the board (GND plane)
    lines.append(f"D{a_fill}*")
    cx, cy = BOARD_W / 2, BOARD_H / 2
    lines.append(f"{gxy(cx, cy)}D03*")
    lines.append(GERBER_FOOTER)
    return "\n".join(lines)


def gen_mask_layer(is_top: bool = True) -> str:
    """Generate solder mask layer (openings at pads, inverted)."""
    lib = ApertureLib()
    MASK_EXP = 0.1  # mask expansion in mm

    pad_ops = []
    for ref, (cx, cy, rot) in CPL_DATA.items():
        fp_func = COMPONENT_FOOTPRINTS.get(ref)
        if not fp_func:
            continue
        for (dx, dy, pw, ph, shape) in fp_func():
            rdx, rdy = rotate(dx, dy, rot)
            px = cx + rdx
            py = cy + rdy
            if shape == "rect":
                aid = lib.define_rect(pw + MASK_EXP, ph + MASK_EXP)
            elif shape == "circle":
                aid = lib.define_circle(pw + MASK_EXP)
            elif shape == "obround":
                aid = lib.define_obround(pw + MASK_EXP, ph + MASK_EXP)
            else:
                aid = lib.define_rect(pw + MASK_EXP, ph + MASK_EXP)
            pad_ops.append((aid, px, py))

    func = "SolderMask,Top" if is_top else "SolderMask,Bot"
    lines = [gerber_header(func, polarity="Negative")]
    lines.append(lib.definitions())
    lines.append("G01*")

    current_apt = -1
    for (aid, px, py) in pad_ops:
        if aid != current_apt:
            lines.append(f"D{aid}*")
            current_apt = aid
        lines.append(f"{gxy(px, py)}D03*")

    lines.append(GERBER_FOOTER)
    return "\n".join(lines)


def gen_silkscreen(is_top: bool = True) -> str:
    """Generate silkscreen with component designators."""
    lib = ApertureLib()
    a_text = lib.define_circle(0.12)  # thin line for text approximation

    func = "Legend,Top" if is_top else "Legend,Bot"
    lines = [gerber_header(func)]
    lines.append(lib.definitions())
    lines.append(f"D{a_text}*")
    lines.append("G01*")

    if is_top:
        # Draw designators as small cross markers at component centres
        for ref, (cx, cy, rot) in CPL_DATA.items():
            half = 0.6
            # Horizontal line
            lines.append(f"{gxy(cx - half, cy)}D02*")
            lines.append(f"{gxy(cx + half, cy)}D01*")
            # Vertical line
            lines.append(f"{gxy(cx, cy - half)}D02*")
            lines.append(f"{gxy(cx, cy + half)}D01*")

        # Board outline on silkscreen
        a_border = lib.define_circle(0.15)
        lines.insert(len(lines) - 1, f"D{a_border}*")
        border = [
            (1.0, 1.0), (54.0, 1.0), (54.0, 54.0), (1.0, 54.0), (1.0, 1.0)
        ]
        lines.append(f"{gxy(border[0][0], border[0][1])}D02*")
        for bx, by in border[1:]:
            lines.append(f"{gxy(bx, by)}D01*")

    lines.append(GERBER_FOOTER)
    return "\n".join(lines)


def gen_drill_file() -> str:
    """Generate Excellon drill file for THT pads and mounting holes."""
    lines = [
        "M48",
        "FMAT,2",
        "METRIC,LZ",
        "TCST,0",
        ";KAGI Lite PCB Drill File",
        ";Generated by KAGI Manufacturing Tools",
        "",
        "T01C0.80  ;Pin headers, JST",
        "T02C1.00  ;Large THT pads (SW2, BZ1)",
        "T03C3.20  ;M3 mounting holes",
        "%",
        "G90",
        "G05",
        "M71",
    ]

    # T01: JST + small THT
    lines.append("T01")
    jst_refs = ["J2"]
    for ref in jst_refs:
        if ref in CPL_DATA:
            cx, cy, rot = CPL_DATA[ref]
            for dx, dy, *_ in COMPONENT_FOOTPRINTS[ref]():
                rdx, rdy = rotate(dx, dy, rot)
                lines.append(f"X{int((cx+rdx)*1000):+07d}Y{int((cy+rdy)*1000):+07d}")

    # T02: SW2 button, BZ1 buzzer
    lines.append("T02")
    tht_refs = ["SW2", "BZ1"]
    for ref in tht_refs:
        if ref in CPL_DATA:
            cx, cy, rot = CPL_DATA[ref]
            for dx, dy, *_ in COMPONENT_FOOTPRINTS[ref]():
                rdx, rdy = rotate(dx, dy, rot)
                lines.append(f"X{int((cx+rdx)*1000):+07d}Y{int((cy+rdy)*1000):+07d}")

    # T03: M3 mounting holes (4 corners)
    lines.append("T03")
    mount_holes = [(3.5, 3.5), (51.5, 3.5), (51.5, 51.5), (3.5, 51.5)]
    for mhx, mhy in mount_holes:
        lines.append(f"X{int(mhx*1000):+07d}Y{int(mhy*1000):+07d}")

    lines.append("M30")
    return "\n".join(lines)


# ── Main: generate all files and zip ─────────────────────────────────────────

def generate_kagi_lite(outdir: str):
    os.makedirs(outdir, exist_ok=True)

    files = {
        "kagi-lite-F_Cu.gbr":    gen_copper_layer("F.Cu", True, True),
        "kagi-lite-In1_Cu.gbr":  gen_copper_inner(2),  # GND plane
        "kagi-lite-In2_Cu.gbr":  gen_copper_inner(3),  # PWR plane
        "kagi-lite-B_Cu.gbr":    gen_copper_layer("B.Cu", False, False),
        "kagi-lite-F_Mask.gbr":  gen_mask_layer(True),
        "kagi-lite-B_Mask.gbr":  gen_mask_layer(False),
        "kagi-lite-F_SilkS.gbr": gen_silkscreen(True),
        "kagi-lite-B_SilkS.gbr": gen_silkscreen(False),
        "kagi-lite-Edge_Cuts.gbr": gen_edge_cuts(),
        "kagi-lite.drl":          gen_drill_file(),
    }

    print(f"Generating KAGI Lite Gerbers → {outdir}/")
    for fname, content in files.items():
        fpath = os.path.join(outdir, fname)
        with open(fpath, "w") as f:
            f.write(content)
        print(f"  ✓ {fname}  ({len(content):,} bytes)")

    # Create ZIP for JLCPCB upload
    zippath = outdir + ".zip"
    with zipfile.ZipFile(zippath, "w", zipfile.ZIP_DEFLATED) as zf:
        for fname in files:
            zf.write(os.path.join(outdir, fname), fname)
    print(f"\n  📦 {zippath}  (JLCPCB Gerber upload)")
    return zippath


