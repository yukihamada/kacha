# ── Coordinate helpers ────────────────────────────────────────────────────────
def gv(mm: float) -> int:
    """Convert mm to Gerber units (6 decimal places)."""
    return int(round(mm * 1_000_000))

def gxy(x: float, y: float) -> str:
    """Format X/Y Gerber coordinate pair."""
    return f"X{gv(x):09d}Y{gv(y):09d}"

# ── Gerber file header / footer ───────────────────────────────────────────────
def gerber_header(file_function: str, polarity: str = "Positive") -> str:
    now = datetime.now().strftime("%Y-%m-%dT%H:%M:%S+09:00")
    return (
        f"G04 KAGI Lite PCB — {file_function}*\n"
        f"G04 Generated by KAGI Manufacturing Tools {datetime.now().strftime('%Y-%m-%d')}*\n"
        "%FSLAX36Y36*%\n"
        "%MOMM*%\n"
        f"%TF.GenerationSoftware,KAGIGen,1.0,kagi-pcb-generator*%\n"
        f"%TF.CreationDate,{now}*%\n"
        f"%TF.ProjectId,kagi-lite,b2000000-0000-0000-0000-000000000001,1.0*%\n"
        f"%TF.FileFunction,{file_function}*%\n"
        f"%TF.FilePolarity,{polarity}*%\n"
        "%LPD*%\n"
    )

GERBER_FOOTER = "M02*\n"

# ── Aperture definitions ──────────────────────────────────────────────────────
class ApertureLib:
    """Manages aperture definitions for a Gerber file."""
    def __init__(self):
        self._apertures: dict[str, int] = {}
        self._next_id = 10

    def get(self, shape: str, *dims) -> int:
        key = f"{shape}:{','.join(str(d) for d in dims)}"
        if key not in self._apertures:
            self._apertures[key] = self._next_id
            self._next_id += 1
        return self._apertures[key]

    def define_circle(self, d: float) -> int:
        return self.get("C", d)

    def define_rect(self, w: float, h: float) -> int:
        return self.get("R", w, h)

    def define_obround(self, w: float, h: float) -> int:
        return self.get("O", w, h)

    def definitions(self) -> str:
        lines = []
        for key, aid in self._apertures.items():
            shape, *dims = key.split(":")
            vals = dims[0].split(",")
            if shape == "C":
                lines.append(f"%ADD{aid}C,{vals[0]}*%")
            elif shape == "R":
                lines.append(f"%ADD{aid}R,{vals[0]}X{vals[1]}*%")
            elif shape == "O":
                lines.append(f"%ADD{aid}O,{vals[0]}X{vals[1]}*%")
        return "\n".join(lines) + "\n"


